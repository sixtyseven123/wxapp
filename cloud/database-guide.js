/**
 * 集合1：test_records（测试记录）
 * 
 * 创建步骤：
 * 1. 点击"添加集合"按钮
 * 2. 输入集合名称：test_records
 * 3. 点击"确定"创建
 * 
 * 权限设置：
 * 点击集合名称进入详情 -> 点击"权限设置" -> 选择"自定义安全规则"
 * 输入以下JSON规则：
 * 
 * {
 *   "read": "doc._openid == auth.openid",
 *   "write": "doc._openid == auth.openid"
 * }
 * 
 * 说明：用户只能读写自己创建的测试记录
 * 
 * 索引设置：
 * 点击"索引管理" -> 点击"添加索引"
 * 
 * 索引1：
 * - 索引名称：createTime_desc
 * - 索引字段：createTime
 * - 排序方向：降序(1)
 * - 是否唯一：否
 * 
 * 索引2：
 * - 索引名称：type_index
 * - 索引字段：type
 * - 排序方向：升序(1)
 * - 是否唯一：否
 */

