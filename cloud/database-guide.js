// ============================================================
// 权限规则详解
// ============================================================
/**
 * 基础权限模式说明：
 * 
 * 1. "所有用户可读，仅创建者可写"
 *    - 所有登录用户可以读取所有记录
 *    - 只有记录创建者可以修改自己的记录
 *    - 规则：{ "read": true, "write": "doc._openid == auth.openid" }
 * 
 * 2. "仅创建者可读写"
 *    - 用户只能读写自己创建的记录
 *    - 规则：{ "read": "doc._openid == auth.openid", "write": "doc._openid == auth.openid" }
 * 
 * 3. "所有用户可读"
 *    - 所有用户可读，但只有管理端（云函数）可写
 *    - 规则：{ "read": true, "write": false }
 * 
 * 4. "所有用户不可读写"
 *    - 只有管理端（云函数）可读写
 *    - 规则：{ "read": false, "write": false }
 * 
 * 变量说明：
 * - auth.openid：当前登录用户的openid（系统自动提供）
 * - doc._openid：记录中的openid字段（创建记录时自动添加）
 */

